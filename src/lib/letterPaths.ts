/** Stroke paths for any traceable character (uppercase A–Z or lowercase a–z). */
export function getLetterStrokes(ch: string): LetterStrokes | undefined {
  return LETTER_PATHS[ch] ?? LOWERCASE_PATHS[ch];
}

/** True if the character has any stroke dipping below the baseline (y > 1). */
export function hasDescender(ch: string): boolean {
  return 'gjpqy'.includes(ch);
}

